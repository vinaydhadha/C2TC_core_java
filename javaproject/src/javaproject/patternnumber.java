package javaproject;

public class patternnumber {
	 
	    public static void main(String[] args)   
	    {  
	        int n=1;  
	        for(int i=0;i<6;i++)  
	        {  
	             for(int j=1;j<i;j++)  
	             {  
	                 System.out.print(" "+n);  
	               n++;  
	             }  
	             System.out.println("");  
	        }  
	    }  
	}  